package blazingforest.abst;

public class QAA {
	
	private String question;
	private String answer;
	
	public QAA(String question, String answer) {
		super();
		this.question = question;
		this.answer = answer;
	}
	
	public String getQuestion() {
		return question;
	}
	public String getAnswer() {
		return answer;
	}
	
}
